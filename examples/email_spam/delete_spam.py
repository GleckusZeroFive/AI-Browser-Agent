"""
Пример: Удаление спама из почтового ящика

Этот скрипт демонстрирует использование AI Browser Agent для
автоматического удаления спама из почтовых ящиков Yandex и Gmail.

Использование:
    python examples/email_spam/delete_spam.py

Примеры задач:
    - "Удали все письма от отправителя spam@example.com"
    - "Очисти папку Спам в Яндекс почте"
    - "Удали все письма с темой 'Акция' старше 30 дней"
    - "Очисти корзину Gmail"
"""
import asyncio
import sys
from pathlib import Path

# Добавляем корневую директорию в путь для импортов
sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from src.dialogue_manager import DialogueManager
from src.system_check import run_system_check
from src.version_info import print_version_info


async def main():
    """Запуск примера удаления спама из почты"""
    print("=" * 70)
    print("📧 AI BROWSER AGENT - УДАЛЕНИЕ СПАМА ИЗ ПОЧТЫ")
    print("=" * 70)
    print("\nПоддерживаемые почтовые сервисы:")
    print("  • Яндекс.Почта (mail.yandex.ru)")
    print("  • Gmail (mail.google.com)")
    print("\nВажно: Для входа в почту потребуется ваш логин и пароль.")
    print("        Агент НЕ сохраняет ваши учетные данные!\n")
    print("=" * 70)

    # Вывод версий
    print_version_info(verbose=False)

    # Проверка системы
    if not run_system_check():
        print("\n❌ Запуск невозможен из-за проблем с системными требованиями.")
        sys.exit(1)

    # Запуск диалога
    manager = DialogueManager()

    # Добавляем специальный промпт для работы с почтой
    manager.agent.add_message("system", """
Ты помощник для работы с электронной почтой. Твоя задача - помочь пользователю управлять почтовым ящиком.

ВАЖНЫЕ ПРАВИЛА:
1. БЕЗОПАСНОСТЬ:
   - НИКОГДА не сохраняй и не логируй пароли пользователя
   - Предупреди пользователя о необходимости ввода учетных данных
   - Работай только с почтовыми сервисами из задачи пользователя

2. РАБОТА С ПОЧТОЙ:
   - Для Яндекс.Почты: открой mail.yandex.ru
   - Для Gmail: открой mail.google.com
   - Дождись авторизации пользователя (он введет данные вручную)
   - После входа можешь работать с письмами

3. УДАЛЕНИЕ ПИСЕМ:
   - Всегда уточняй критерии: отправитель, тема, дата
   - Показывай количество писем перед удалением
   - Проси подтверждение перед массовым удалением
   - Умей работать с папками: "Входящие", "Спам", "Корзина"

4. ПРИМЕРЫ КОМАНД:
   - "Удали все письма от example@spam.com"
   - "Очисти папку Спам"
   - "Удали письма старше 30 дней"
   - "Переместить письма от X в Спам"

Будь вежлив и осторожен с данными пользователя!
""")

    print("\n🤖 Агент готов к работе с почтой!")
    print("\nПримеры задач:")
    print("  • 'Открой Яндекс почту и удали спам'")
    print("  • 'Зайди в Gmail и очисти корзину'")
    print("  • 'Удали все письма от spam@example.com'")
    print("\n(Напиши задачу или 'exit' для выхода)\n")

    # Запускаем основной цикл диалога
    await manager.start()


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        print("\n\n👋 Программа завершена.")
    except Exception as e:
        print(f"\n\n❌ Критическая ошибка: {e}")
        sys.exit(1)
