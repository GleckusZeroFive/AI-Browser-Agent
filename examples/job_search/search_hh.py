"""
Пример: Поиск вакансий на HeadHunter (hh.ru)

Этот скрипт демонстрирует использование AI Browser Agent для
автоматического поиска и анализа вакансий на hh.ru.

Использование:
    python examples/job_search/search_hh.py

Примеры задач:
    - "Найди вакансии Python разработчика в Москве"
    - "Покажи удаленные вакансии для Senior Python Developer с зарплатой от 200000"
    - "Найди вакансии Data Scientist в IT компаниях"
    - "Покажи джуниор вакансии по JavaScript в Санкт-Петербурге"
"""
import asyncio
import sys
from pathlib import Path

# Добавляем корневую директорию в путь для импортов
sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from src.dialogue_manager import DialogueManager
from src.system_check import run_system_check
from src.version_info import print_version_info


async def main():
    """Запуск примера поиска вакансий на hh.ru"""
    print("=" * 70)
    print("💼 AI BROWSER AGENT - ПОИСК ВАКАНСИЙ НА HH.RU")
    print("=" * 70)
    print("\nВозможности:")
    print("  • Поиск вакансий по ключевым словам")
    print("  • Фильтрация по городу и зарплате")
    print("  • Анализ требований и описаний")
    print("  • Поиск удаленной работы")
    print("  • Фильтрация по опыту работы\n")
    print("=" * 70)

    # Вывод версий
    print_version_info(verbose=False)

    # Проверка системы
    if not run_system_check():
        print("\n❌ Запуск невозможен из-за проблем с системными требованиями.")
        sys.exit(1)

    # Запуск диалога
    manager = DialogueManager()

    # Добавляем специальный промпт для поиска вакансий
    manager.agent.add_message("system", """
Ты помощник для поиска работы на сайте hh.ru (HeadHunter). Твоя задача - помочь пользователю найти подходящие вакансии.

ВАЖНЫЕ ПРАВИЛА:
1. РАБОТА С HH.RU:
   - Всегда начинай с открытия hh.ru
   - Используй поисковую строку для ввода запросов
   - Применяй фильтры для уточнения результатов
   - Умей работать с расширенным поиском

2. КРИТЕРИИ ПОИСКА:
   - Должность/специализация (Python developer, Data Scientist и т.д.)
   - Город (Москва, Санкт-Петербург, удаленная работа)
   - Уровень: Junior, Middle, Senior, Lead
   - Зарплата: от, до, валюта
   - Тип занятости: полная, частичная, удаленная
   - Опыт работы: без опыта, 1-3 года, 3-6 лет, 6+ лет

3. АНАЛИЗ ВАКАНСИЙ:
   - Читай описания вакансий
   - Выделяй ключевые требования
   - Сообщай о зарплатных ожиданиях
   - Проверяй тип занятости и формат работы
   - Отмечай бонусы и преимущества

4. ПРИМЕРЫ КОМАНД:
   - "Найди Python разработчика в Москве"
   - "Покажи удаленные вакансии для Data Scientist"
   - "Найди Junior вакансии с зарплатой от 100000"
   - "Какие требования у топовых вакансий по ML?"

5. РЕЗУЛЬТАТЫ:
   - Показывай название вакансии, компанию, зарплату
   - Суммируй основные требования
   - Предлагай открыть интересные вакансии для деталей
   - Можешь составить список подходящих вакансий

Будь полезен и помогай пользователю найти работу мечты!
""")

    print("\n🤖 Агент готов к поиску вакансий!")
    print("\nПримеры задач:")
    print("  • 'Найди вакансии Python разработчика в Москве'")
    print("  • 'Покажи удаленные вакансии для Senior Backend Developer'")
    print("  • 'Найди вакансии Data Scientist с зарплатой от 200000'")
    print("\n(Напиши задачу или 'exit' для выхода)\n")

    # Запускаем основной цикл диалога
    await manager.start()


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        print("\n\n👋 Программа завершена.")
    except Exception as e:
        print(f"\n\n❌ Критическая ошибка: {e}")
        sys.exit(1)
